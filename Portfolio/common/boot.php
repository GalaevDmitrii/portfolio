                <h2 class="text-center text-black p_b">Qu'est-ce que Bootstrap ?</h2>
                <p class="text-center text-black p_b">

                    Nous savons donc qu'il est utile, mais à quoi sert-il, et comment aide-t-il vraiment les développeurs web ? La boîte à outils de développement d'applications Web a été créée par Mark Otto et Jacob Thornton, deux anciens employés de Twitter (ci-dessous).<br>

                    Les deux créateurs de Bootstrap, les développeurs Web Mark Otto et Jacob Thornton, debout à côté d'un mur blanc.<br>

                    Le site officiel de Bootstrap le décrit comme :<br>

                    "Le framework HTML, CSS et JS le plus populaire pour développer des projets responsive, mobile-first sur le web".<br>

                    Voici ce que cela signifie en termes plus clairs :<br>

                    Bootstrap est une collection géante de bouts de code pratiques et réutilisables écrits en HTML, CSS et JavaScript. Il s'agit également d'un cadre de développement frontal qui permet aux développeurs et aux concepteurs de créer rapidement des sites Web entièrement réactifs.<br>

                    Essentiellement, Bootstrap vous évite d'écrire beaucoup de code CSS, ce qui vous laisse plus de temps à consacrer à la conception de pages Web.<br>

                    Il est également GRATUIT !<br>

                    Il est actuellement hébergé sur GitHub et peut être téléchargé facilement sur le site officiel.<br>

                    Donc, maintenant que nous savons ce que c'est, regardons de plus près pour voir s'il est utile pour votre travail de développement web.<br>

                    Pourquoi Bootstrap est-il la référence pour les développeurs Web ?<br>
                    Pourquoi devriez-vous éviter Bootstrap ?<br>
                    En résumé
                    Pourquoi Bootstrap est-il la solution de choix pour les développeurs Web ?<br>
                    Décomposons les avantages en 8 parties :<br>

                    1. Sa grille réactive<br>
                    Plus besoin de passer des heures à coder votre propre grille : Bootstrap est livré avec son propre système de grille prédéfini. Maintenant, vous pouvez directement remplir vos conteneurs avec du contenu. Définir des points d'arrêt personnalisés pour chaque colonne est un jeu d'enfant grâce aux points d'arrêt extra small, small, medium, large et extra large. Vous pouvez également vous contenter de la grille par défaut, car elle répond peut-être déjà aux besoins de votre site.<br>

                    2. Ses images réactives<br>
                    Bootstrap dispose de son propre code pour redimensionner automatiquement les images en fonction de la taille actuelle de l'écran. Il suffit d'ajouter la classe .img-responsive à vos images, et les règles CSS prédéfinies s'occupent du reste.<br>

                    Laissez Bootstrap redimensionner vos images pour vous !<br>

                    Il peut même modifier la forme de vos images en ajoutant des classes telles que img-circle et img-rounded, et ce, sans avoir à faire d'allers-retours entre le code et votre logiciel de conception.<br>

                    3. Ses composants<br>
                    Bootstrap est livré avec toute une série de composants que vous pouvez facilement intégrer à votre page Web, notamment :<br>

                    des barres de navigation<br>
                    Des listes déroulantes<br>
                    Barres de progression<br>
                    Vignettes<br>
                    Il est non seulement très facile d'ajouter des éléments de conception attrayants à votre page Web, mais vous pouvez également être sûr que chacun d'entre eux sera parfait, quelle que soit la taille de l'écran ou le périphérique utilisé pour les afficher. Vous disposez ainsi d'une multitude de fonctionnalités prêtes à l'emploi.<br>

                    Pour une liste plus complète des fonctionnalités ajoutées, consultez la documentation du composant.<br>

                    4. Son JavaScript<br>
                    Toujours pas assez de fonctions ? Essayez JQuery !<br>

                    Bootstrap permet également aux développeurs de tirer parti de plus d'une douzaine de plugins JQuery personnalisés. Cette bibliothèque vous donne encore plus d'espace pour jouer avec l'interactivité, offrant des solutions faciles pour les popups modales, les transitions, les carrousels d'images et - l'un de mes préférés - un plugin appelé scrollspy, qui met automatiquement à jour votre barre de navigation lorsque vous faites défiler une page.<br>

                    5. Sa documentation<br>
                    La documentation de Bootstrap est tout simplement l'une des meilleures que nous ayons jamais vues. Chaque élément de code est décrit et expliqué en détail sur son site Web.<br>

                    Les explications comprennent également des exemples de code pour une mise en œuvre de base, ce qui simplifie le processus, même pour le plus débutant des débutants. Tout ce que vous avez à faire est de choisir un composant, de copier et coller le code dans votre page, et de le modifier à partir de là.<br>

                    6. Sa personnalisation<br>
                    L'une des principales critiques à l'égard des frameworks tels que Bootstrap est leur taille - le poids qu'ils représentent peut vraiment ralentir votre application au premier chargement. La version actuelle du fichier CSS de Bootstrap, par exemple, pèse 119 Ko. Bien que cela ne semble pas particulièrement important par rapport aux fichiers image et vidéo, pour un fichier CSS, c'est énorme !<br>

                    Ce que le site vous permet de faire pour combattre ce problème, cependant, c'est de personnaliser la fonctionnalité que vous voulez inclure dans votre téléchargement. En vous rendant simplement sur la page Personnaliser et télécharger, vous pouvez cocher les fonctions dont vous n'avez pas besoin pour votre application, ce qui allège le poids de votre fichier et évite à vos utilisateurs un temps de chargement supplémentaire.<br>

                    La personnalisation est la clé !<br>

                    7. Sa communauté<br>
                    Comme c'est le cas pour de nombreux projets open-source, Bootstrap est soutenu par une grande communauté de concepteurs et de développeurs. Le fait d'être hébergé sur GitHub permet aux développeurs de modifier et de contribuer facilement à la base de code de Bootstrap. Il est également facile pour les gens de collaborer, de donner leur avis et d'interagir avec leurs pairs et leurs collègues utilisateurs.<br>

                    Bootstrap possède une page Twitter active, un blog Bootstrap et même une salle Slack dédiée. Sans parler de la multitude de développeurs prêts à aider en cas de problèmes techniques sur Stack Overflow, où toutes les questions peuvent être trouvées sous le tag bootstrap-4.<br>

                    8. Ses modèles externes<br>
                    Au fur et à mesure que sa popularité augmentait, les gens ont commencé à créer des modèles basés sur Bootstrap afin d'accélérer la création de sites Web.<br>


